#Receba 3 numeros, faça a soma entre eles e calcule o valor da soma ao quadrado

Num1 = int(input("Insira o numero 1: "))
Num2 = int(input("Insira o numero 2: "))
Num3 = int(input("Insira o numero 3: "))

Soma = Num1+Num2+Num3
Soma = Soma**2

print(f"O valor da soma ao quadrado dos 3 numeros é {Soma}")