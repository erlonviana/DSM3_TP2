#5. Ler dois valores e efetuar adição, subtração, divisão e multiplicação entre eles

Valor1 = float(input("Insira o valor 1: "))
Valor2 = float(input("Insira o valor 2: "))

Adicao = Valor1 + Valor2
Subtracao = Valor1 - Valor2
Multiplicacao = Valor1 * Valor2
Divisao = Valor1 / Valor2

print(f"O valor da adição é {Adicao:.2f}")
print(f"O valor da subtração é {Subtracao:.2f}")
print(f"O valor da multiplicação é {Multiplicacao:.2f}")
print(f"O valor da divisão é {Divisao:.2f}")